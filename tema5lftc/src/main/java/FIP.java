public class FIP {
    private String atom;
    private int cod;

    public FIP(String atom, int cod) {
        this.atom = atom;
        this.cod = cod;
    }

    public String getAtom() {
        return atom;
    }

    public void setAtom(String atom) {
        this.atom = atom;
    }

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }
}
